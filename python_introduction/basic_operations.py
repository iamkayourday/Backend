# Basic Arithmetic Exercise
number1 = 10
number2 = 5

# Addition
sum = number1 + number2
print(f"Addition of {number1} and {number2} is: {sum}")

# Subtraction
difference = number1 - number2
print(f"Subtraction of {number1} and {number2} is: {difference}")

# Multiplication
multiplication = number1 * number2
print(f"Multiplication of {number1} and {number2} is: {multiplication}")